@st.cache_data
def load(name):
    path = os.path.join(CSV_DIR, f"{name}.csv")
    if os.path.exists(path):
        return pd.read_csv(path)
    return pd.DataFrame()

def fmt_cr(val):
    """Format large numbers as ₹ Cr or ₹ Lakh."""
    if val >= 1e7:
        return f"₹{val/1e7:.2f} Cr"
    elif val >= 1e5:
        return f"₹{val/1e5:.2f} L"
    return f"₹{val:,.0f}"

def fmt_num(val):
    if val >= 1e9:
        return f"{val/1e9:.2f}B"
    elif val >= 1e6:
        return f"{val/1e6:.2f}M"
    elif val >= 1e3:
        return f"{val/1e3:.2f}K"
    return f"{val:,.0f}"

