# ─────────────────────────────────────────────────────────────
#  APPLY FILTERS
# ─────────────────────────────────────────────────────────────
def apply_filters(df):
    if df.empty:
        return df
    if sel_years and "Year" in df.columns:
        df = df[df["Year"].isin(sel_years)]
    if sel_quarters and "Quarter" in df.columns:
        df = df[df["Quarter"].isin(sel_quarters)]
    if sel_states and "State" in df.columns:
        df = df[df["State"].isin(sel_states)]
    return df

